package br.unb.unbomber.component;


public class ControlPair {

	private int key;
	
	private Direction command;

	public int getKey() {
		return key;
	}

	public void setKey(int key) {
		this.key = key;
	}

	public Direction getCommand() {
		return command;
	}

	public void setCommand(Direction command) {
		this.command = command;
	}

}
